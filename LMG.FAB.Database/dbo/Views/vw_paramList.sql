AS
SELECT     dbo.paramTable.pk_paramTable, dbo.paramTable.code, dbo.paramTable.libelleLong, dbo.paramTable.libelleCourt, dbo.paramTable.codeDefaut, 
                      dbo.paramDetail.pk_paramDetail, dbo.paramDetail.fk_paramTable, dbo.paramDetail.code AS detail_code, dbo.paramDetail.libelleLong AS detail_libelleLong, 
                      dbo.paramDetail.libelleCourt AS detail_libelleCourt, dbo.paramDetail.tri, dbo.paramDetail.actif
FROM         dbo.paramDetail INNER JOIN
                      dbo.paramTable ON dbo.paramDetail.fk_paramTable = dbo.paramTable.pk_paramTable

